def test_v1_report_empty_state_is_fully_formed() -> None:
    out = render_v1_report(_empty_state_replay_data())

    assert "# algotrading-paper / replay" in out
    assert "Variant — null" in out
    assert "0 trades" in out
    assert "[run log →](runs/2026-05-02T160629.log)" in out

    assert "**—**" in out, "em-dash should mark not-yet-present slot"
    assert "**0**" in out, "zero is a real value, must render explicitly"
    assert "**$0.00**" in out, "zero P&L is real"
    assert "**100%**" in out

    assert "§ 01 — Per-variant performance" in out
    assert "no variants registered" in out

    assert "§ 02 — Bars in period · by symbol" in out
    assert "no bars yet" in out
    assert "§ 03 — Run summary" in out
    assert "§ 04 — Notes" in out

    assert "§ Flags · none" in out, "flags section collapsed when empty"

    assert out.rstrip().endswith(
        "[github.com/pwysocan-droid/algotrading-paper](https://github.com/pwysocan-droid/algotrading-paper)"
    )

    assert "no data yet" not in out
    assert "TODO" not in out


def test_v1_report_does_not_use_zero_where_value_is_unknown() -> None:
    """The em-dash convention is load-bearing.

    "Variants registered" is em-dash because the slot exists but no
    variants are *yet* registered. "Trades in period" is 0 because zero
    trades happened — that's a real measurement.
    """
    out = render_v1_report(_empty_state_replay_data())
    band_section = out.split("# algotrading-paper / replay")[1]
    band_section = band_section.split("§ 01")[0]
    variants_cell_idx = band_section.find("Variants registered")
    assert variants_cell_idx >= 0
    next_table_row = band_section.find("**", variants_cell_idx)
    assert "**—**" in band_section[next_table_row : next_table_row + 80]


def test_v1_report_with_flags_puts_them_above_table() -> None:
    data = _empty_state_replay_data()
    data["flags"] = ["Run failed at 2026-05-02T14:00 — Alpaca returned 503"]
    out = render_v1_report(data)
    flag_idx = out.find("🔴 § Flags · 1 active")
    table_idx = out.find("§ 01 — Per-variant performance")
    assert flag_idx >= 0
    assert table_idx > flag_idx, "flags above the dominant table when populated"


def test_v1_report_section_markers_use_section_sign() -> None:
    out = render_v1_report(_empty_state_replay_data())
    section_lines = [line for line in out.split("\n") if line.startswith("## ")]
    for line in section_lines:
        assert "§" in line, f"section header without § marker: {line!r}"


def test_v1_report_missing_required_key_raises() -> None:
    data = _empty_state_replay_data()
    del data["title"]
    with pytest.raises(ValueError, match="missing required key"):
        render_v1_report(data)


