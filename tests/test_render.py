"""Tests for render.py format conventions.

Non-negotiable: the empty-state report must be fully-formed (header, four-stat
band, dominant table with placeholder, footer) — em-dashes for not-yet-present
values, explicit zeros where zero is real.
"""

from __future__ import annotations

from datetime import datetime, timezone

import pytest

import render
from render import (
    EMDASH,
    Stat,
    TableSpec,
    format_count,
    format_currency,
    format_human_time,
    format_iso_ts,
    format_pct,
    format_ratio,
    format_sparkline,
    format_trade_id,
    format_variant_name,
    render_flags_section,
    render_four_stat_band,
    render_index,
    render_marginalia_footer,
    render_v1_report,
)


def test_format_currency_zero_is_real() -> None:
    assert format_currency(0.0) == "$0.00"
    assert format_currency(0) == "$0.00"


def test_format_currency_emdash_for_none() -> None:
    assert format_currency(None) == EMDASH


def test_format_currency_negative_and_positive() -> None:
    assert format_currency(-38.20) == "-$38.20"
    assert format_currency(112.40) == "$112.40"
    assert format_currency(1247.34) == "$1,247.34"


def test_format_pct_signs() -> None:
    assert format_pct(0.0) == "0.00%"
    assert format_pct(5.23) == "+5.23%"
    assert format_pct(-3.8) == "-3.80%"
    assert format_pct(None) == EMDASH


def test_format_count_zero_is_real() -> None:
    assert format_count(0) == "0"
    assert format_count(347) == "347"
    assert format_count(None) == EMDASH


def test_format_ratio() -> None:
    assert format_ratio(1.34) == "1.34"
    assert format_ratio(-0.21) == "-0.21"
    assert format_ratio(None) == EMDASH


def test_format_iso_ts() -> None:
    dt = datetime(2026, 5, 2, 16, 6, 29, tzinfo=timezone.utc)
    assert format_iso_ts(dt) == "2026-05-02T16:06:29Z"
    assert format_iso_ts(None) == EMDASH


def test_format_human_time() -> None:
    dt = datetime(2026, 5, 2, 16, 6, tzinfo=timezone.utc)
    assert format_human_time(dt) == "May 02, 16:06 UTC"
    assert format_human_time(None) == EMDASH


def test_format_variant_name() -> None:
    assert format_variant_name("bollinger_default") == "`bollinger_default`"
    assert format_variant_name(None) == EMDASH
    assert format_variant_name("") == EMDASH


def test_format_trade_id() -> None:
    assert (
        format_trade_id(142)
        == "[`#0142`](https://app.alpaca.markets/paper/orders/142)"
    )
    assert format_trade_id(None) == EMDASH
    assert format_trade_id(None, proposed=True) == "[#proposed]"
    assert format_trade_id("abc-123") == "[`#abc-123`](https://app.alpaca.markets/paper/orders/abc-123)"


def test_format_sparkline_basic() -> None:
    line = format_sparkline([1.0, 2.0, 3.0, 4.0])
    assert len(line) == 4
    assert all(c in "▁▂▃▄▅▆▇█" for c in line)
    assert line[0] == "▁"
    assert line[-1] == "█"


def test_format_sparkline_empty() -> None:
    assert format_sparkline([]) == EMDASH
    assert format_sparkline(None) == EMDASH


def test_format_sparkline_flat() -> None:
    line = format_sparkline([5.0, 5.0, 5.0])
    assert len(line) == 3
    assert len(set(line)) == 1


def test_four_stat_band_requires_exactly_four() -> None:
    with pytest.raises(ValueError, match="exactly 4"):
        render_four_stat_band([Stat("A", "1")])
    with pytest.raises(ValueError, match="exactly 4"):
        render_four_stat_band([Stat(f"L{i}", str(i)) for i in range(5)])


def test_four_stat_band_shape() -> None:
    stats = [
        Stat("Variants registered", EMDASH, "0 enabled"),
        Stat("Trades in period", "0", "paper"),
        Stat("P&L", "$0.00", EMDASH + "%"),
        Stat("System uptime", "100%", "last 4w"),
    ]
    out = render_four_stat_band(stats)
    lines = out.split("\n")
    assert len(lines) == 4
    assert lines[0] == "| Variants registered | Trades in period | P&L | System uptime |"
    assert lines[1] == "| :--- | :--- | :--- | :--- |"
    assert "**—**" in lines[2]
    assert "**0**" in lines[2]
    assert "**$0.00**" in lines[2]
    assert "0 enabled" in lines[3]
    assert "paper" in lines[3]


