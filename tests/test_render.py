def test_render_index_idempotent() -> None:
    state = {
        "phase": "Phase 1",
        "week": 1,
        "timestamp": datetime(2026, 5, 2, 16, 6, 29, tzinfo=timezone.utc),
        "latest_links": [],
        "stats": [
            Stat("A", EMDASH, "—"),
            Stat("B", "0", "—"),
            Stat("C", "0", "—"),
            Stat("D", "0", "—"),
        ],
        "surfaces": [],
        "reading_order": [("a", "b")],
        "foundational_docs": [("PROJECT.md", "PROJECT.md")],
    }
    a = render_index(state)
    b = render_index(state)
    assert a == b
